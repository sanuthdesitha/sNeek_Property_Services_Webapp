/**
 * Escape a string for safe interpolation into HTML (email bodies, server-rendered
 * templates). Prevents HTML/script injection from untrusted user input.
 */
export function escapeHtml(value: unknown): string {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
