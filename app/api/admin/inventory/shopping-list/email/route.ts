import { NextRequest, NextResponse } from "next/server";
import { NotificationChannel, NotificationStatus, Role } from "@prisma/client";
import { z } from "zod";
import { requireRole } from "@/lib/auth/session";
import { db } from "@/lib/db";
import { getAppSettings } from "@/lib/settings";
import { sendEmailDetailed } from "@/lib/notifications/email";
import {
  buildShoppingListHtml,
  getShoppingListGrouped,
  renderShoppingListPdf,
} from "@/lib/inventory/shopping-list-report";

const schema = z.object({
  scope: z.string().optional().default("all"),
  toEmail: z.string().trim().email().optional(),
  userId: z.string().cuid().optional(),
  subject: z.string().trim().min(1).optional(),
});

function sanitizeScope(scope: string): string {
  return scope.trim() || "all";
}

export async function POST(req: NextRequest) {
  try {
    const session = await requireRole([Role.ADMIN, Role.OPS_MANAGER]);
    const body = schema.parse(await req.json().catch(() => ({})));
    const scope = sanitizeScope(body.scope);

    let recipientEmail: string | null = null;
    let recipientName: string | null = null;

    if (body.userId) {
      const user = await db.user.findUnique({
        where: { id: body.userId },
        select: { id: true, name: true, email: true, isActive: true },
      });
      if (!user || !user.isActive) {
        return NextResponse.json({ error: "Selected user is not available." }, { status: 404 });
      }
      recipientEmail = user.email;
      recipientName = user.name ?? user.email;
    } else if (body.toEmail) {
      recipientEmail = body.toEmail;
      recipientName = body.toEmail;
    } else {
      return NextResponse.json({ error: "Select a user or enter a custom email address." }, { status: 400 });
    }

    const [grouped, settings] = await Promise.all([getShoppingListGrouped(scope), getAppSettings()]);
    const property = scope === "all"
      ? null
      : await db.property.findUnique({ where: { id: scope }, select: { name: true } });
    const scopeLabel = scope === "all" ? "All properties" : property?.name ?? "Selected property";

    const html = buildShoppingListHtml({
      companyName: settings.companyName,
      logoUrl: settings.logoUrl,
      scopeLabel,
      grouped,
      recipientName,
    });

    let pdf: Buffer;
    try {
      pdf = await renderShoppingListPdf(html);
    } catch {
      return NextResponse.json(
        { error: "Could not generate shopping list PDF. Ensure Playwright browsers are installed." },
        { status: 500 }
      );
    }

    const generatedDate = new Date().toISOString().slice(0, 10);
    const fileLabel = scope === "all" ? "all-properties" : scope;
    const filename = `shopping-list-${fileLabel}-${generatedDate}.pdf`;
    const subject = body.subject ?? `${settings.companyName} - Inventory Shopping List (${scopeLabel})`;
    const emailResult = await sendEmailDetailed({
      to: recipientEmail,
      subject,
      html: `
        <p>Hello ${recipientName ?? "there"},</p>
        <p>Your inventory shopping list is attached as a PDF.</p>
        <p><strong>Scope:</strong> ${scopeLabel}</p>
        <p>Generated by ${settings.companyName}.</p>
      `,
      attachments: [{ filename, content: pdf }],
    });

    await db.notification.create({
      data: {
        userId: session.user.id,
        channel: NotificationChannel.EMAIL,
        subject,
        body: `Shopping list (${scopeLabel}) sent to ${recipientEmail}`,
        status: emailResult.ok ? NotificationStatus.SENT : NotificationStatus.FAILED,
        sentAt: emailResult.ok ? new Date() : undefined,
        errorMsg: emailResult.ok ? undefined : emailResult.error ?? "Email provider returned failure.",
      },
    });

    if (!emailResult.ok) {
      return NextResponse.json({ error: emailResult.error ?? "Could not send email." }, { status: 502 });
    }

    return NextResponse.json({ ok: true, sentTo: recipientEmail });
  } catch (err: any) {
    const status = err.message === "UNAUTHORIZED" ? 401 : err.message === "FORBIDDEN" ? 403 : 400;
    return NextResponse.json({ error: err.message }, { status });
  }
}
